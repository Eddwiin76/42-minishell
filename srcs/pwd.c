#include "../includes/minishell.h"

void	ft_handle_pwd(void)
{
	char pwd[4096];

	getcwd(pwd, 4096);
	g_status = 0;
	ft_putstr_fd(pwd, 1);
	ft_putstr_fd("\n", 1);
}
